'''MACROS'''

OFFSET = [[-1, 0], [0, 1], [1, 0], [0, -1]]
PASSED = 0
CACHE_DEFAULT_VALUE = -0xFFFFFFFF

'''VARIABLES'''
M, N = 0, 0
arr = []
visited = []
cache = []
START_POSTIION = (0, 0)

'''UTILS'''

def print_arr(_arr, N, M):
    for i in range(N):
        for j in range(M):
            print(_arr[i][j], end=" ")
        print()    

def is_overflow(x, y, M, N):
    return x < 0 or x >= M or y < 0 or y >= N

def is_downhill(x, y, new_x, new_y, arr):
    return arr[x][y] > arr[new_x][new_y]

def is_visited(x, y, visited):
    return visited[x][y]

def DFS(curr, M, N, arr, cache, visited):

    x, y = curr
    count = 0
    # BASE CASE
    if x == M - 1 and y == N - 1:
        return 1

    # Cache HIT
    if cache[x][y] != CACHE_DEFAULT_VALUE:
        return cache[x][y]

    # Cache MISS
    for dir in range(len(OFFSET)):
        new_x = x + OFFSET[dir][0]
        new_y = y + OFFSET[dir][1]

        # Pass uphill road, the place where cannot reach, or already visited
        if is_overflow(new_x, new_y, M, N) \
            or not is_downhill(x, y, new_x, new_y, arr) \
            or is_visited(new_x, new_y, visited):
            continue

        visited[x][y] = True
        count += DFS((new_x, new_y), M, N, arr, cache, visited)
        visited[x][y] = False
        cache[x][y] = count
        
    return count

